func makeAdmin(accountId, groupId bson.ObjectId) error {
	r := &mongomodels.Relationship{
		Id:         bson.NewObjectId(),
		TargetId:   accountId,
		TargetName: "JAccount",
		SourceId:   groupId,
		SourceName: "JGroup",
		As:         "admin",
		TimeStamp:  time.Now().UTC(),
	}

	return modelhelper.AddRelationship(r)
}
